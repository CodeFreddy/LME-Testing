$ErrorActionPreference = 'Stop'

$repoRoot = Split-Path -Parent (Split-Path -Parent $MyInvocation.MyCommand.Path)
$handoffPath = Join-Path $repoRoot 'docs\session_handoff.md'

$branch = (git -C $repoRoot branch --show-current).Trim()
if (-not $branch) {
    $branch = 'unknown'
}

$recentCommitSubjects = @(git -C $repoRoot log -5 --pretty=format:%s)
$generatedAt = (Get-Date).ToUniversalTime().ToString("yyyy-MM-dd HH:mm:ss 'UTC'")

$recentCommitsBlock = if ($recentCommitSubjects.Count -gt 0) {
    ($recentCommitSubjects | ForEach-Object { "- ``$_``" }) -join [Environment]::NewLine
} else {
    "- No commits found"
}

$template = @'
