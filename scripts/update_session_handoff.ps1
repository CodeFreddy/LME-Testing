> This file is auto-generated by `scripts/update_session_handoff.ps1`.
> It is refreshed on commit when local git hooks are enabled.

## Purpose

This file is a short handoff for continuing work on another machine or in a fresh AI session.

It captures the current repo state, the most relevant documents, the recent change direction, and the recommended next step.

Generated at:

- `{0}`

---

## Read First

